Map::Map() {
	dirt = TextureManager::LoadTexture("assets/dirt.png");
	grass = TextureManager::LoadTexture("assets/grass.png");
	water = TextureManager::LoadTexture("assets/water.png");

	LoadMap(lvl1);

	srcRect.x = srcRect.y = 0;
	srcRect.w = srcRect.h = Game::TILE_SIZE;
	destRect.w = destRect.h = Game::TILE_SIZE * Game::PIXEL_SCALE;
	destRect.x = destRect.y = 0;
}

void Map::LoadMap(int arr[20][25]) {
	for (int row = 0; row < 20; row++)
	{
		for (int column = 0; column < 25; column++)
		{
			map[row][column] = arr[row][column];
		}
	}
}

void Map::DrawMap() {
	int type = 0;

	for (int row = 0; row < 20; row++)
	{
		for (int column = 0; column < 25; column++)
		{
			type = map[row][column];

			destRect.x = column * Game::TILE_SIZE * Game::PIXEL_SCALE;
			destRect.y = row * Game::TILE_SIZE * Game::PIXEL_SCALE;

			switch (type)
			{
			case 0:
				break;
			case 1:
				TextureManager::Draw(dirt, srcRect, destRect);
				break;
			case 2:
				TextureManager::Draw(grass, srcRect, destRect);
				break;
			case 3:
				TextureManager::Draw(water, srcRect, destRect);
				break;
			default:
				break;
			}
		}
	}
}